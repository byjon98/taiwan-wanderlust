files = ['src/data-1.ts', 'src/data-2.ts', 'src/data-3.ts', 'src/data-4.ts', 'src/data-extra.ts']

for filepath in files:
    with open(filepath, 'r') as f:
        content = f.read()
    
    updated = False
    for name, (lat, lng) in COORDS.items():
        # Match the name in the object and insert coordinates
        escaped_name = re.escape(name)
        pattern = r"(\{\s*n:\s*'" + escaped_name + r"',)"
        replacement = r"\1 lat: " + str(lat) + r", lng: " + str(lng) + r","
        new_content = re.sub(pattern, replacement, content)
        if new_content != content:
            content = new_content
            updated = True
            print(f"  Added coords for: {name}")
    
    if updated:
        with open(filepath, 'w') as f:
            f.write(content)

print("Done!")
